test_that("autoqmd_insert_children errors on malformed markers in QMD", {
  # Create a malformed QMD template (missing end marker)
  qmd <- "malformed_template.qmd"
  writeLines(
    c(
      "# Test Document",
      "",
      "<!-- AUTOGENERATED-START -->",
      "Some content without an end marker"
    ),
    qmd
  )

  # Expect an error due to malformed markers
  expect_error(
    autoqmd_insert_children(
      target_file = qmd,
      child_files = child_files,
      quiet = TRUE
    ),
    "Markers not found or malformed"
  )

  # Cleanup
  unlink(qmd)
})


