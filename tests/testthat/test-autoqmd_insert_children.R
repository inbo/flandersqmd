    expect_true(any(grepl(paste0("file: ", f), txt, fixed = TRUE)))
  }

  # 5. Entries must appear only within the autogenerated block
  start <- which(grepl("AUTOGENERATED-START", txt))
  end   <- which(grepl("AUTOGENERATED-END", txt))
  expect_true(start < end)

  outside_block <- txt[-(start:end)]
  expect_false(any(grepl("file:", outside_block)))

  # 6. Test verbosity
  expect_message(
    autoqmd_insert_children(
      target_file  = yml,
      child_files  = child_files,
      child_labels = child_labels,
      start_marker = "# AUTOGENERATED-START",
      end_marker   = "# AUTOGENERATED-END"
    ),
    fixed = TRUE,
    "Updated autogenerated block in _quarto_copy.yml with 2 sidebar entries."
  )
})

