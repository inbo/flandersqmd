  expect_equal(output, normalize(txt))

  # 2. Markers must still be present
  expect_true(any(grepl("AUTOGENERATED-START", txt)))
  expect_true(any(grepl("AUTOGENERATED-END", txt)))

  # 3. Each label should appear exactly once
  for (label in child_labels) {
    expect_true(any(grepl(paste0("text: ", label), txt, fixed = TRUE)))
  }

  # 4. Each child file should appear exactly once
